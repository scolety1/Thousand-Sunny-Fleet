function Write-GameForgeLines {
    param(
        [string]$Path,
        [object[]]$Lines
    )

    New-Item -ItemType Directory -Force -Path (Split-Path -Parent $Path) | Out-Null
    $cleanLines = @($Lines)
    while ($cleanLines.Count -gt 0 -and [string]::IsNullOrWhiteSpace([string]$cleanLines[$cleanLines.Count - 1])) {
        if ($cleanLines.Count -eq 1) {
            $cleanLines = @()
        } else {
            $cleanLines = @($cleanLines[0..($cleanLines.Count - 2)])
        }
    }
    $cleanLines | Set-Content -LiteralPath $Path -Encoding UTF8
    return $Path
}

