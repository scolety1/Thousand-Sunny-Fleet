param()

$ErrorActionPreference = "Stop"

function Resolve-GameForgePath {
    param(
        [string]$Root,
        [string]$Path
    )

    if ([string]::IsNullOrWhiteSpace($Path)) {
        return $Path
    }

    if ([System.IO.Path]::IsPathRooted($Path)) {
        return $Path
    }

    return Join-Path $Root $Path
}

function ConvertTo-GameForgeSlug {
    param([string]$Name)

    $slug = ([string]$Name).Trim().ToLowerInvariant()
    $slug = [regex]::Replace($slug, "[^a-z0-9]+", "-").Trim("-")
    if ([string]::IsNullOrWhiteSpace($slug)) {
        return "game-project"
    }

    return $slug
}

function Get-GameForgeIntakeValue {
    param(
        [string]$Text,
        [string]$Label,
        [string]$Default = "unknown"
    )

    $match = [regex]::Match($Text, "(?im)^\s*-\s+$([regex]::Escape($Label))\s*:\s*(.+?)\s*$")
    if ($match.Success) {
        return $match.Groups[1].Value.Trim()
    }

    return $Default
}

function Get-GameForgeIntake {
    param(
        [string]$FleetRoot,
        [string]$IntakePath
    )

    $fleetRootFull = (Resolve-Path -LiteralPath $FleetRoot).Path
    if ([string]::IsNullOrWhiteSpace($IntakePath)) {
        $IntakePath = Join-Path $fleetRootFull "tests\fixtures\fleet\game-forge\nytheria-intake.md"
    } else {
        $IntakePath = Resolve-GameForgePath -Root $fleetRootFull -Path $IntakePath
    }

    if (!(Test-Path -LiteralPath $IntakePath)) {
        throw "Game Forge intake not found: $IntakePath"
    }

    $text = Get-Content -LiteralPath $IntakePath -Raw -ErrorAction Stop
    $name = Get-GameForgeIntakeValue -Text $text -Label "Game name" -Default "Nytheria"

    return [pscustomobject]@{
        sourcePath = $IntakePath
        rawText = $text
        gameName = $name
        slug = ConvertTo-GameForgeSlug -Name $name
        aliases = Get-GameForgeIntakeValue -Text $text -Label "Old names / aliases" -Default "none"
        genre = Get-GameForgeIntakeValue -Text $text -Label "Genre" -Default "unknown"
        camera = Get-GameForgeIntakeValue -Text $text -Label "Camera/view style" -Default "unknown"
        mode = Get-GameForgeIntakeValue -Text $text -Label "Single-player/co-op/multiplayer" -Default "unknown"
        platform = Get-GameForgeIntakeValue -Text $text -Label "Target platform" -Default "unknown"
        promise = Get-GameForgeIntakeValue -Text $text -Label "Core fantasy / player promise" -Default "unknown"
        world = Get-GameForgeIntakeValue -Text $text -Label "World structure" -Default "unknown"
        combat = Get-GameForgeIntakeValue -Text $text -Label "Combat style" -Default "unknown"
        progression = Get-GameForgeIntakeValue -Text $text -Label "Progression style" -Default "unknown"
        aiNeeds = Get-GameForgeIntakeValue -Text $text -Label "AI/living-world needs" -Default "unknown"
        canon = Get-GameForgeIntakeValue -Text $text -Label "Lore/canon constraints" -Default "unknown"
        saveLoad = Get-GameForgeIntakeValue -Text $text -Label "Save/load expectations" -Default "unknown"
        pipeline = Get-GameForgeIntakeValue -Text $text -Label "Modding/content pipeline needs" -Default "unknown"
        prototypeGoal = Get-GameForgeIntakeValue -Text $text -Label "Prototype goal" -Default "unknown"
        offLimits = Get-GameForgeIntakeValue -Text $text -Label "Off-limits" -Default "full engine until toy simulations prove the loop"
        mustAskTim = Get-GameForgeIntakeValue -Text $text -Label "Must-ask-Tim decisions" -Default "canon and prototype priority"
    }
}

