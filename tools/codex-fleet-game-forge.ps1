        $OutDirectory = Resolve-GameForgePath -Root $fleetRootFull -Path $OutDirectory
    }

    $path = Join-Path $OutDirectory "$($intake.slug)-game-work-orders.md"
    Write-GameForgeLines -Path $path -Lines (New-GameForgeWorkOrderLines -Intake $intake)
}

function Write-GameForgePack {
    param(
        [string]$FleetRoot,
        [string]$IntakePath
    )

    $fleetRootFull = (Resolve-Path -LiteralPath $FleetRoot).Path
    return [pscustomobject]@{
        blueprint = Write-GameForgeEngineBlueprint -FleetRoot $fleetRootFull -IntakePath $IntakePath
        systemsMap = Write-GameForgeSystemsMap -FleetRoot $fleetRootFull -IntakePath $IntakePath
        prototypeSlices = Write-GameForgePrototypeSlices -FleetRoot $fleetRootFull -IntakePath $IntakePath
        researchPrompts = Write-GameForgeResearchPrompts -FleetRoot $fleetRootFull -IntakePath $IntakePath
        riskReview = Write-GameForgeRiskReview -FleetRoot $fleetRootFull -IntakePath $IntakePath
        workOrders = Write-GameForgeWorkOrders -FleetRoot $fleetRootFull -IntakePath $IntakePath
    }
}
